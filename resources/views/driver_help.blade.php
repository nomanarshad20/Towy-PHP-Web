    <h1 id="header-content" class="_c6 _c7 _bl _mq _mr" data-baseweb="typo-headingxlarge">How to request a ride</h1>
</div>
<div class="_mk _mp">
    <div class="_bo _bp _bq _br " data-baseweb="block">
        <div>
            <ol>
                <li>Open your app and enter your destination in the &ldquo;Where to?&rdquo; field.</li>
                <li>Swipe up to see all vehicle options available in your area. Tap one to select it for your ride.</li>
                <li>You may be asked to confirm your pickup location. If so, move the pin on the map. Tap &ldquo;Confirm Pickup&rdquo; to send your request to eligible drivers in the area.</li>
                <li>You&rsquo;ll see an estimated time to be matched with an eligible driver. If no drivers are available for the ride you&rsquo;ve chosen, the app will suggest an alternate ride type with available drivers nearby.</li>
                <li>Once a driver accepts your request, you&rsquo;ll see their location and estimated arrival time on the map.</li>
            </ol>
            <div class="_mk _mp">
